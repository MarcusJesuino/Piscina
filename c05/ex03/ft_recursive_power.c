int ft_recursive_power(int nb, int power)
{
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	else
		return (nb * ft_recursive_power(nb, --power));
}
#include <stdio.h>
int main(void)
{
	printf ("%d", ft_recursive_power(2, 3));
	return (0);
}
